let angka  = 100

switch (parseInt(angka)) {
    case 1:
        console.log("Ini angka 1")
        break;
    case 2:
        console.log("Ini angka 2")
        break;
    case 3:
        console.log("Ini angka 3")
        break;
    case 4:
        console.log("Ini angka 4")
        break;
    default:
        console.log("Angka berapa itu kocak")
        break;
}